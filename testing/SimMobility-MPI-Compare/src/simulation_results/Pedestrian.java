package simulation_results;

public class Pedestrian
{

	private int id;
	private long x_pos;
	private long y_pos;

	public int getId()
	{
		return id;
	}

	public void setId(int id)
	{
		this.id = id;
	}

	public long getX_pos()
	{
		return x_pos;
	}

	public void setX_pos(long xPos)
	{
		x_pos = xPos;
	}

	public long getY_pos()
	{
		return y_pos;
	}

	public void setY_pos(long yPos)
	{
		y_pos = yPos;
	}

}
