package obj;

public class DrawElement {

	private int element_id;

	private double from_x;
	private double from_y;
	private double to_x;
	private double to_y;

	private int partition_id;

	public int getElement_id() {
		return element_id;
	}

	public void setElement_id(int elementId) {
		element_id = elementId;
	}

	public double getFrom_x() {
		return from_x;
	}

	public void setFrom_x(double fromX) {
		from_x = fromX;
	}

	public double getFrom_y() {
		return from_y;
	}

	public void setFrom_y(double fromY) {
		from_y = fromY;
	}

	public double getTo_x() {
		return to_x;
	}

	public void setTo_x(double toX) {
		to_x = toX;
	}

	public double getTo_y() {
		return to_y;
	}

	public void setTo_y(double toY) {
		to_y = toY;
	}

	public int getPartition_id() {
		return partition_id;
	}

	public void setPartition_id(int partitionId) {
		partition_id = partitionId;
	}

}
